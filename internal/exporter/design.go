	writer.WriteString("SET UNIQUE_CHECKS = 0;\n")
	writer.WriteString("SET @OLD_FOREIGN_KEY_CHECKS = @@FOREIGN_KEY_CHECKS;\n")
	writer.WriteString("SET FOREIGN_KEY_CHECKS = 0;\n")
	writer.WriteString("SET @OLD_SQL_MODE = @@SQL_MODE;\n")
	writer.WriteString("SET SQL_MODE = 'NO_AUTO_VALUE_ON_ZERO';\n")
	writer.WriteString("SET @OLD_SQL_NOTES = @@SQL_NOTES;\n")
	writer.WriteString("SET SQL_NOTES = 0;\n")
	writer.WriteString("SET @OLD_AUTOCOMMIT = @@AUTOCOMMIT;\n")
	writer.WriteString("SET AUTOCOMMIT = 0;\n")
	writer.WriteString("SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci;\n")
	writer.WriteString("SET SESSION collation_connection = 'utf8mb4_unicode_ci';\n\n")

	// =========================
	// TABLE STRUCTURES
	// =========================
	writer.WriteString("-- ============================================\n")
	writer.WriteString("-- TABLE STRUCTURES\n")
	writer.WriteString("-- ============================================\n\n")

	for _, table := range tables {
		var tableName, createTable string
		query := fmt.Sprintf("SHOW CREATE TABLE `%s`", table)
		err := d.db.QueryRow(query).Scan(&tableName, &createTable)
		if err != nil {
			return fmt.Errorf("failed to get create table for %s: %v", table, err)
		}

		writer.WriteString(fmt.Sprintf("-- Table: %s\n", table))
		writer.WriteString(createTable)
		writer.WriteString(";\n\n")

		// Indexes
		indexes, err := d.getTableIndexes(table)
		if err == nil && len(indexes) > 0 {
			writer.WriteString(fmt.Sprintf("-- Indexes for %s\n", table))
			for _, index := range indexes {
				writer.WriteString(index)
				writer.WriteString(";\n")
			}
			writer.WriteString("\n")
		}
	}

	// =========================
	// RESTORE SESSION SETTINGS
	// =========================
	writer.WriteString("-- ------------------------------------------------------\n")
	writer.WriteString("-- Restore safe session settings\n")
	writer.WriteString("-- ------------------------------------------------------\n")

	writer.WriteString("/*!40103 SET TIME_ZONE='+00:00' */;\n")
	writer.WriteString("/*!40101 SET SQL_MODE='NO_AUTO_VALUE_ON_ZERO' */;\n")
	writer.WriteString("/*!40014 SET FOREIGN_KEY_CHECKS=1 */;\n")
	writer.WriteString("/*!40014 SET UNIQUE_CHECKS=1 */;\n")
	writer.WriteString("/*!40101 SET CHARACTER_SET_CLIENT=utf8mb4 */;\n")
	writer.WriteString("/*!40101 SET CHARACTER_SET_RESULTS=utf8mb4 */;\n")
	writer.WriteString("/*!40101 SET COLLATION_CONNECTION=utf8mb4_unicode_ci */;\n")
