	// =========================
	writer.WriteString("-- ------------------------------------------------------\n")
	writer.WriteString("-- MySQL Database Export\n")
	writer.WriteString(fmt.Sprintf("-- Database: %s\n", d.databaseName))
	writer.WriteString("-- Generated by DesignExporter\n")
	writer.WriteString(fmt.Sprintf("-- Date: %s\n", time.Now().Format("2006-01-02 15:04:05")))
	writer.WriteString("-- Compatible with MySQL 5.7+ / 8.0+\n")
	writer.WriteString("-- ------------------------------------------------------\n\n")

	writer.WriteString("SET @OLD_CHARACTER_SET_CLIENT = @@CHARACTER_SET_CLIENT;\n")
	writer.WriteString("SET @OLD_CHARACTER_SET_RESULTS = @@CHARACTER_SET_RESULTS;\n")
	writer.WriteString("SET @OLD_COLLATION_CONNECTION = @@COLLATION_CONNECTION;\n")
	writer.WriteString("SET NAMES utf8mb4;\n")
	writer.WriteString("SET @OLD_TIME_ZONE = @@TIME_ZONE;\n")
	writer.WriteString("SET TIME_ZONE = '+00:00';\n")
